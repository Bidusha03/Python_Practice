# Program to find the total number of seconds in a day

# Total number of hours in a day
hours_in_day = 24

# Total number of minutes in an hour
minutes_in_hour = 60

# Total number of seconds in a minute
seconds_in_minute = 60

# Calculate the total number of seconds in a day
total_seconds_in_day = hours_in_day * minutes_in_hour * seconds_in_minute

# Display the result
print("Total number of seconds in a day:", total_seconds_in_day)


